    private void perder() {
        JOptionPane.showMessageDialog(this, "La palabra era " + solucion, "Â¡Perdiste!", JOptionPane.ERROR_MESSAGE);
        setBotones(false);
    }

    private void actualizar(Palabra nueva) {
        p = nueva;
        this.vistaPalabra1.actualizar(p);
        this.vistaVidas1.actualizar(MAX_ERRORES - errores);
        this.vistaDibujo1.actualizar(errores);
        if (p.acertada()) {
            JOptionPane.showMessageDialog(this, "Â¡Ganaste!", "Ahorcado", JOptionPane.INFORMATION_MESSAGE);
            setBotones(false);
        } else if (errores == MAX_ERRORES) {
            perder();
        }
        this.repaint();
        this.revalidate();
    }

    private String pedirPalabra() {
        SolicitudPalabra solicitud = new SolicitudPalabra(this, true);
        if (solicitud == null) {
            this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
        }
        String resultado = solicitud.getPalabra();
        return resultado;
    }

    private void iniciarJuego() {
        solucion = pedirPalabra();
        errores = 0;
        Palabra p = new Palabra(solucion);
        setBotones(true);
        actualizar(p);
    }

